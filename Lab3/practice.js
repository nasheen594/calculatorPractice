class Bag{
    color=null;
    length= 50;
    material= null;

    Bag(){

        this.color=color;
        this.length=length;
        this.material=this.material;
    }

    Bag(doubleTheLength){
        this.color=color;
        this.length=length*doubleTheLength;
        this.material=this.material;
    }
}

var bagpack= new Bag();
var purse= new Bag(0.5);